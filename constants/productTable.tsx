import { IComment } from "@/models/product";
import { ColumnDef } from "@tanstack/react-table";
import Link from "next/link";

export type Product = {
  id: string;
  price: number;
  name: string;
  status: "inStock" | "outStock";
  count: number;
  oldPrice?: number;
  category?: string;
  description: string;
  createdAt?: string;
  updatedAt?: string;
  colors?: string[];
  tags?: string[];
  images?: string[];
  comments?: IComment[];
};

export const columns: ColumnDef<Product>[] = [
  {
    accessorKey: "name",
    header: "Name",
    cell: ({ getValue, row }: any) => {
      const count = row.original.count;
      return (
        <Link
          href={`/dashboard/product/edit/${row.original.id}`}
          className=" rounded-lg w-fit"
        >
          {getValue()}
        </Link>
      );
    },
  },
  {
    accessorKey: "status",
    header: "Status",
    cell: ({ getValue, row }: any) => {
      const count = row.original.count;
      const status = count
        ? count < 10
          ? "Low Stock"
          : "In Stock"
        : "Out Of Stock";
      let color: string;

      if (count > 10) {
        color = "green";
      } else if (count > 0) {
        color = "black";
      } else {
        color = "red";
      }

      return (
        <div
          style={{
            color: color,
            padding: "4px",
            backgroundColor:
              color === "red" ? "#fdd" : color === "black" ? "gray" : "#ddf",
          }}
          className=" rounded-lg w-max"
        >
          {status}
        </div>
      );
    },
  },
  {
    accessorKey: "count",
    header: "Count",
    cell: ({ getValue, row }: any) => {
      const count = row.original.count;
      return <div className=" rounded-lg w-fit">{count || 0}</div>;
    },
  },
  {
    accessorKey: "price",
    header: "Price",
  },

  {
    accessorKey: "createdBy",
    header: "Created By",
  },
  {
    accessorKey: "createdAt",
    header: "Created At",
  },
  {
    accessorKey: "updatedAt",
    header: "Updated At",
  },
];
